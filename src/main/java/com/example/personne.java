package com.example;

public class personne {
    String nom;
    String Prenom;
    String Age;
    String Sexe;
    String Adresse;
    String CodePostale;
    String Ville;

    public personne(String nom, String Prenom, String Age, String Sexe, String Adresse, String CodePostale, String Ville) {
        this.nom = nom;
        this.Prenom = Prenom;
        this.Age = Age;
        this.Sexe = Sexe;
        this.Adresse = Adresse;
        this.CodePostale = CodePostale;
        this.Ville = Ville;
    }
}
